test "encrypt random data with ECB" {
    const keyLong: u64 = 0x133457799BBCDFF1;
    const testKey = mem.asBytes(&keyLong);
    var da = &std.heap.DirectAllocator.init();
    defer da.deinit();
    var allocator = &da.allocator;
    const tmp_file_name = "random_test_data_small.bin";

    var file = try os.File.openRead(tmp_file_name);
    defer file.close();

    var file_in_stream = file.inStream();
    var buf_stream = io.BufferedInStream(os.File.ReadError).init(&file_in_stream.stream);
    const st = &buf_stream.stream;
    const contents = try st.readAllAlloc(allocator, 3000000 + 1);
    defer allocator.free(contents);

    var encryptedData = try allocator.alloc(u8, contents.len);
    des.desEncryptEcb(testKey, contents[0..], encryptedData);

    var digest = Sha1.init();
    digest.update(encryptedData);
    var out: [Sha1.digest_length]u8 = undefined;
    digest.final(out[0..]);

